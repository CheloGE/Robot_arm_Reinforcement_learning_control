import numpy as np
import random
import copy
import torch
from collections import namedtuple, deque

class OUNoise:
    """
        Ornstein-Uhlenbeck process. This noise generates random samples from a 
        Gaussian (Normal) distribution, but each sample affects the next one such 
        that two consecutive samples are more likely to be closer together than further apart. 
        In this sense, the process in Markovian in nature

        We want to use Ornstein-Uhlenbeck process to add some noise to our actions, in order to encourage exploratory behavior.

        Since our actions translate to force/torque being applied to motors from the robot, we want consecutive actions to not vary wildly. 
        Otherwise, we may not actually get anywhere! The controller in this case the DDPG_agent cannot jump from one value to another, that
        would be very unstable.
    """

    def __init__(self, size, seed, mu=0., theta=0.15, sigma=0.2):
        """Initialize parameters and noise process."""
        self.mu = mu * np.ones(size)
        self.theta = theta
        self.sigma = sigma
        self.seed = random.seed(seed)
        self.reset()

    def reset(self):
        """Reset the internal state (= noise) to mean (mu)."""
        self.state = copy.copy(self.mu)

    def sample(self):
        """Update internal state and return it as a noise sample."""
        x = self.state
        dx = self.theta * (self.mu - x) + self.sigma * np.array([random.random() for i in range(len(x))])
        self.state = x + dx
        return self.state

class ReplayBuffer:
    """Fixed-size buffer to store experience tuples."""

    def __init__(self, action_size, buffer_size, batch_size, seed):
        """Initialize a ReplayBuffer object.
        Params
        ======
            buffer_size (int): maximum size of buffer
            batch_size (int): size of each training batch
        """
        self.action_size = action_size
        self.memory = deque(maxlen=buffer_size)  # internal memory (deque)
        self.batch_size = batch_size
        self.experience = namedtuple("Experience", field_names=["state", "action", "reward", "next_state", "done"])
        self.seed = random.seed(seed)
    
    def add(self, state, action, reward, next_state, done):
        """Add a new experience to memory."""
        e = self.experience(state, action, reward, next_state, done)
        self.memory.append(e)
    
    def sample(self):
        """Randomly sample a batch of experiences from memory."""
